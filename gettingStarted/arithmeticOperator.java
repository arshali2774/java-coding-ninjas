package gettingStarted;

public class arithmeticOperator {
    public static void main(String[] args) {

        System.out.println(9 + 5);
        System.out.println(9 - 5);
        System.out.println(9 / 5);
        System.out.println(9 * 5);

        System.out.println(9 % 5);

        System.out.println('a' + 'b');

        char c = 'a';
        char i = (char) (c + 3);
        System.out.println(i);
    }
}
